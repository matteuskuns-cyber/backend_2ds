export function carro(nomeCarro, dataDeCompra, preco)
{
    return `Você comprou o ${nomeCarro}  dia ${dataDeCompra} e pago ${preco}`
}
export function corDoCarro(nomeCarro, CorDaTinta, preco)
{
    return `O  ${nomeCarro} foi pintado dia ${CorDaTinta} por ${preco}`
}
export function modificacao(nomeCarro, tipoBorykit, preco)
{
    return `O  ${nomeCarro} foi modificado ${tipoBorykit} por ${preco}`
}